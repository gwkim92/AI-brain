export type TranslationKey = keyof typeof translations.en;

export function getLocaleTag(locale: AppLocale): string {
  return LOCALE_TAGS[locale];
}

export function detectLocaleFromAcceptLanguage(acceptLanguage?: string | null): AppLocale {
  if (typeof acceptLanguage === "string" && /\bko(?:-|_|,|;|$)/i.test(acceptLanguage)) {
    return "ko";
  }
  return "en";
}

export function detectLocaleFromBrowser(): AppLocale {
  if (typeof window === "undefined") {
    return "en";
  }
  const languages = Array.from(
    new Set([window.navigator.language, ...(window.navigator.languages ?? [])].filter(Boolean))
  );
  if (languages.some((value) => value.toLowerCase().startsWith("ko"))) {
    return "ko";
  }
  const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone;
  if (timezone === "Asia/Seoul") {
    return "ko";
  }
  return "en";
}

export function readLocalePreference(): AppLocalePreference {
  if (typeof window === "undefined") {
    return "auto";
  }
  try {
    const raw = window.localStorage.getItem(APP_LOCALE_STORAGE_KEY);
    if (raw === "ko" || raw === "en") {
      return raw;
    }
  } catch {
    // ignore storage failures
  }
  return "auto";
}

export function writeLocalePreference(preference: AppLocalePreference): void {
  if (typeof window === "undefined") {
    return;
  }
  try {
    if (preference === "auto") {
      window.localStorage.removeItem(APP_LOCALE_STORAGE_KEY);
    } else {
      window.localStorage.setItem(APP_LOCALE_STORAGE_KEY, preference);
    }
  } catch {
    // ignore storage failures
  }
  window.dispatchEvent(new CustomEvent(APP_LOCALE_CHANGED_EVENT, { detail: { preference } }));
}

export function resolveLocale(preference: AppLocalePreference, fallbackLocale: AppLocale): AppLocale {
  if (preference === "ko" || preference === "en") {
    return preference;
  }
  if (typeof window === "undefined") {
    return fallbackLocale;
  }
  return detectLocaleFromBrowser();
}

export function translate(locale: AppLocale, key: TranslationKey, values?: Record<string, string | number>): string {
  const template = translations[locale][key] ?? translations.en[key] ?? key;
  if (!values) return template;
  let message = template as string;
  for (const [name, value] of Object.entries(values)) {
    message = message.replaceAll(`{{${name}}}`, String(value));
  }
  return message;
}
